package com.pipe.utils;

/**
 * Encapsulate 2D point.
 *
 * @author Yaniv Zolicha
 */
public class Point{



	/**
	 * The x-axis point.
	 */
	public final int x;



	/**
	 * The y-axis point.
	 */
	public final int y;



	/**
	 * Create new {@link Point} with the specified x and y coordinates.
	 */
	public Point(int x, int y){
		this.x = x;
		this.y = y;
	}



}
